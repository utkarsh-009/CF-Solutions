#include <bits/stdc++.h>
using namespace std;

void solve()
{
    int n;
    n = 5;
    vector<char> v(n);

    for (int x : v)
    {
        cout << x << " ";
    }
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--)
    {
        solve();
        cout << "\n";
    }
}

